// .........the if-else statements..........
// The if statement allows you to create a program that runs only if a specific condition is met.
// The else statement is used to run code only when the if statement is not fulfilled.
//  else if statement allows you to write another condition to check if statement condition isn't met.

let balance=2000;
if(balance>4000)
{
    console.log("you have enough money for the vacation");
}
else if(balance>3000)
{
    console.log("you have money just for short trip");
}
else
{
    console.log("not enough money");
}
console.log("The end");